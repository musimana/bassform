<h3 class="text-small-print">
    YOUR DATA PROTECTION RIGHTS
</h3>

<p class="text-small-print">
    We would like to make sure you are fully aware of all of your data protection rights.
    Everyone is entitled to the following:
</p>

<ul class="text-small-print">
    <li>
        <strong>The right to access</strong> – you have the right to request we for copies of your personal data.
        We may charge you a small fee for this service.
    </li>

    <li>
        <strong>The right to rectification</strong> – you have the right to request that we correct any information you believe is inaccurate.
        You also have the right to request we to complete the information you believe is incomplete.
    </li>

    <li>
        <strong>The right to erasure</strong> – you have the right to request that we erase your personal data, under certain conditions.
    </li>

    <li>
        <strong>The right to restrict processing</strong> – you have the right to request that we restrict the processing of your personal data,
        under certain conditions.
    </li>

    <li>
        <strong>The right to object to processing</strong> – you have the right to object to we’s processing of your personal data,
        under certain conditions.
    </li>

    <li>
        <strong>The right to data portability</strong> – you have the right to request that we transfer the data that we have collected
        to another organization, or directly to you, under certain conditions.
    </li>
</ul>

<p class="text-small-print">
    We aim to respond to all legitimate requests within one month, however, on occasions it may take longer than a month if your request
    is particularly complex or you have made a number of requests.
    In this case, we will notify you and keep you updated throughout the process.
</p>
